int main () {
    int rank = 2;
    int grade;

  switch(rank) {
    case 1 :
      grade = 100;
      break;
    case 2 :
      grade = 90;
      break;
    case 3 :
      grade = 80;
      break;
    case 4 :
      grade = 70;
      break;
    default :
      grade = 0;
      break;
   }
   
   return grade;
}
